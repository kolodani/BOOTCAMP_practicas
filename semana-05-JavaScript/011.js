/*
    Sentencia condicional if - else
*/

var nombre = "Pablo";
var estadoCivil = "soltero";
var estaCasado = true;

if (estadoCivil === "casado") {
    console.log(nombre + " esta " + estadoCivil);
}else {
    console.log(nombre + " esta " + estadoCivil);
}

if (estaCasado) {
    console.log(nombre + " esta casado");
}else {
    console.log(nombre + " esta soltero");
}